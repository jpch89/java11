/*
变量的使用

1. 格式：
数据类型 变量名 = 变量值;

*/
class VariableTest {
    public static void main(String[] args) {
        // 变量的定义
        int myAge = 12;
        // 变量的使用
        System.out.println(myAge);

        // 变量的声明
        int myNumber;
        // 变量的赋值
        myNumber = 101;
        // 变量的使用
        System.out.println(myNumber);
    }
}
